age = 21 # se le asigna un valor a la variable, con esto estamos marcando un parametro
if age >= 20: # si se cumple con la edad, entonces si puedes acceder de lo contrario te mandara un mensaje y no podras acceder
	print('Puedes entrar, cumples con la edad.') # este solo imprime el mensaje cuando se cumple la condicion 
else: # cuando no se cumple la condicion se activa el else
	print('No entras amigo, eres menor de edad crece ya.') # se imprime el mensaje reiterando que no cumples con los parametros
