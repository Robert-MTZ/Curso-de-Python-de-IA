# Ejercicio 01
print ("Ejercicio 01\n")

a = 0 # creamos una variable que inicie en cero

while a <= 80: # ponemos un while, el cual nos indicara hasta donde llegara nuestro numero
   print(a) # este print nos permitira ver todos los numeros por los que se ira incrementando nuestra variable
   a += 4 # establecemos como ira incrementando en este caso sera de 4 en 4

